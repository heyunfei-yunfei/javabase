package day03;
/**本类用来测试循环结构for循环*/
public class Test_For {
	public static void main(String[] args) {
		//需求：打印0到10
		//for（开始条件；循环条件：更改条件）{循环体}
		for(int i=0;i<10;i++){
			System.out.println("闻道有先后，术业有专攻，关关雎鸠，在河之洲窈窕淑女，君子好逑");
		}
		switch("10"){
		case "1":System.out.println("haha");break;
		case "2":System.out.println("止规");break;
		case "3":System.out.println("吃火锅");break;
		case "4":System.out.println("吃螃蟹");break;
		case "5":System.out.println(" 穿透");break;
		case "10":System.out.println("补充代码");break;
		default: System.out.println("总共");
		}
	}
			
}
